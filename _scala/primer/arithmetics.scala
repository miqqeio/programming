println(10 + 20);                       // 30
println(20 - 10);                       // 10
println(10 * 20);                       // 200
println(20 / 10);                       // 2

// floating point conversion
println(10.0 + (10 + 20));              // 40.0
println(20.0 - (10 + 10));              // 0.0
println(10.0 * (10 * 2));               // 200.0

// common division error
println(30 / 20);                       // 1
println(30.0 / 20.0);                   // 1.5

// modulo, remainder
println(12.5 % 10.0);                   // 2.5
println(10.0 % 20.0);                   // 10.0