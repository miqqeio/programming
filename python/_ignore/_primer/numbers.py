print(12 + 2 * (40 / 4) + 10)
# 42

# power
print(10 ** 2)
# 100

# floating-point conversion
print(12.0 + (10 + 20))
# 42.0

# integer divison
print(42 // 40)
# 1

# modulo
print(42 % 40)
# 2

# built-ins
print(abs(-42))
# 42
print(sum([1, 2, 3, 4]))
# 10
print(min(1, 2, 3, 4))
# 1
print(max(1, 2, 3, 4))
# 4

# rounding value
print(round(42.42))
# 42
print(round(42.42, 1))
# 42.4